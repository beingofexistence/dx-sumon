// Looping through 1 to 100
for (let number = 1; number <= 100; number++) {
    // if number is devided by 2 then it must a even number
    if (number % 2 == 0) {
        console.log(`${number} is even`);
    } 
    // or it must be odd number
    else {
        console.log(`${number} is odd`);
    }
}
